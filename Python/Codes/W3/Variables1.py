# If you want to specify the data type of a variable, this can be done with casting.
x = str(3)    # x will be '3'
y = int(3)    # y will be 3
z = float(3)  # z will be 3.0
# You can get the data type of a variable with the type() function.
print(type(x))
print(type(y))
print(type(z))
#Variable names are case-sensitive.
